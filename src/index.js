import { Provider } from 'react-redux';
import { createStore } from 'redux';
import App from './App';
import reducer from './store';

const store = createStore(reducer);

ReactDOM.render(
  <React.StrictMode>
    <Provider store={store}>
      <App />
    </Provider>
  </React.StrictMode>,
  document.getElementById('root'), // document.querySelector('#root')와 같음
);

